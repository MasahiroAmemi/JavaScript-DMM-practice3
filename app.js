//演算子

//足し算
alert(3 + 4); //7
//引き算
alert(8 - 5); //3
//掛け算
alert(2 * 6); //12
//割り算
alert(10 / 2); //5
//余り
alert(10 % 3); //1

//代入演算子

let a = 6;  //aに6を代入
a += 2; //a = a + 2 と同じ
console.log(a); //8

let b =  6; //bに6を代入
b -= 2; //b = b - 2と同じ
console.log(b); //4

let c = 6; //cに6を代入
c *= 2; //c = c * 2と同じ
console.log(c); //12

